function appendCategoryData(){

    let parentCategorydiv1 = document.querySelector(".parentCategorydiv1")

    arr.forEach(function(ele){
        for(let key in ele){

            let childDiv = document.createElement("div")
            childDiv.append(key)
            childDiv.setAttribute("id", "childDiv")
            childDiv.addEventListener("mouseover", function(){

                let parentCategorydiv2 = document.querySelector(".parentCategorydiv2")
                parentCategorydiv2.innerHTML = ""

                for(let innerKey in ele[key]){

                    let childDiv2 = document.createElement("div")
                    childDiv2.id = "childDiv2"
                    childDiv2.append(innerKey)
                    childDiv2.addEventListener("mouseover", function(){

                        let parentCategorydiv3 = document.querySelector(".parentCategorydiv3")
                        parentCategorydiv3.innerHTML = ""

                        for(let innerList of ele[key][innerKey]){
                            let childDiv3 = document.createElement("div")
                            childDiv3.id = "childDiv3"
                            childDiv3.append(innerList)
                            parentCategorydiv3.append(childDiv3)
                        }
                    })
                    parentCategorydiv2.append(childDiv2)
                }
                
            })
            parentCategorydiv1.append(childDiv)

        }
    })
    
}

